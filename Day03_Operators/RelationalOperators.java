class RelationOperators{
    public static void main(String args[]){
        int a = 34;
        int b = 31;

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}